class Platform extends Phaser.Physics.Matter.Image {
    constructor(scene, x, y, verts) {
        super(scene.matter.world, x, y, '', '', {
            'shape': {
                'type': 'fromVertices',
                'verts': verts
            },
            'isStatic': true
        });
    }
}

export class Arena {
    constructor(scene, platforms) {
        for (const name in platforms) {
            const platform = new Platform(scene, platforms[name].x, platforms[name].y, platforms[name].verts);
            this[name] = platform;
        }
    }
}